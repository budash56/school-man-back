// Provides CRUD endpoints for students using the generated Students entity.
import {
  Body,
  Controller,
  Delete,
  Get,
  NotFoundException,
  Param,
  Patch,
  Post,
} from '@nestjs/common';
import type { DeepPartial } from 'typeorm';
import { Students } from './students.entity';
import { StudentsRepository } from './students.repository';

@Controller('students')
export class StudentsController {
  constructor(private readonly repository: StudentsRepository) {}

  @Get()
  findAll() {
    return this.repository.find();
  }

  @Get(':id')
  async findOne(@Param('id') id: string) {
    const entity = await this.repository.findOne({
      where: { studentId: id },
    });

    if (!entity) {
      throw new NotFoundException('Students record not found');
    }

    return entity;
  }

  @Post()
  create(@Body() payload: DeepPartial<Students>) {
    const entity = this.repository.create(payload);
    return this.repository.save(entity);
  }

  @Patch(':id')
  async update(@Param('id') id: string, @Body() payload: DeepPartial<Students>) {
    const entity = await this.findOne(id);
    this.repository.merge(entity, payload);
    return this.repository.save(entity);
  }

  @Delete(':id')
  async remove(@Param('id') id: string) {
    const entity = await this.findOne(id);
    await this.repository.remove(entity);
    return { deleted: true };
  }
}
